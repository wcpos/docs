#!/usr/bin/env node
/**
 * Regenerates the pinned in-app UI label glossary in scripts/translation-context.md.
 *
 * Why this exists: the translation pipeline used to render UI labels freely, so the
 * same button ended up with a different name on every page — and each re-translation
 * rolled the dice again. Docs must name the button the app actually ships, so the
 * source of truth is wcpos/translations (the app's own translation files), not the
 * translator's judgement.
 *
 * Usage:  node scripts/build-ui-glossary.js [--check]
 *   --check  exit 1 if the committed table is stale (for CI), write nothing
 *
 * Locale codes differ between the two repos: docs uses BCP-47-ish tags (pt-BR),
 * the app uses WP-style tags (pt_BR), and for some languages the app only ships a
 * regional variant (it_IT, ko_KR). LOCALES maps docs tag -> app tag.
 */

const fs = require('fs');
const path = require('path');

const REPO = 'wcpos/translations';
const REF = 'main';
const CONTEXT_FILE = path.join(__dirname, 'translation-context.md');
const START = '<!-- UI-GLOSSARY:START - generated by scripts/build-ui-glossary.js, do not edit by hand -->';
const END = '<!-- UI-GLOSSARY:END -->';

// docs locale -> app locale directory
const LOCALES = {
  ar: 'ar',
  de: 'de',
  es: 'es',
  fr: 'fr',
  'hi-IN': 'hi_IN',
  it: 'it_IT',
  ja: 'ja',
  ko: 'ko_KR',
  nl: 'nl',
  'pt-BR': 'pt_BR',
  'zh-CN': 'zh_CN',
};

/**
 * Curated on purpose. Only labels whose meaning in the docs unambiguously refers to
 * this app's UI belong here — a generic word like "Edit" or "Type" appears in the
 * docs about WooCommerce core screens too, and pinning it would push the POS app's
 * wording onto text describing someone else's UI.
 */
const TERMS = [
  { en: 'Checkout', bundle: 'monorepo/core.json', key: 'pos_checkout.checkout', note: 'POS checkout button' },
  { en: 'Checkout', bundle: 'woocommerce-pos/wp-admin-settings.json', key: 'common.checkout', note: 'WP-Admin settings tab — often NOT the same word as the button' },
  { en: 'Cashier', bundle: 'monorepo/core.json', key: 'common.cashier' },
  { en: 'Add Coupon', bundle: 'monorepo/core.json', key: 'coupons.add_coupon' },
  { en: 'Discount', bundle: 'monorepo/core.json', key: 'pos_cart.discount' },
  { en: 'Refund', bundle: 'monorepo/core.json', key: 'orders.refund' },
  { en: 'Date Created', bundle: 'monorepo/core.json', key: 'common.date_created' },
  { en: 'Price', bundle: 'monorepo/core.json', key: 'common.price' },
  { en: 'Total', bundle: 'monorepo/core.json', key: 'common.total' },
  { en: 'Actions', bundle: 'monorepo/core.json', key: 'common.actions' },
];

const url = (locale, bundle) =>
  `https://raw.githubusercontent.com/${REPO}/${REF}/translations/js/${locale}/${bundle}`;

const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

async function fetchBundle(locale, bundle, attempt = 1) {
  const MAX = 4;
  try {
    const res = await fetch(url(locale, bundle));
    if (!res.ok) throw new Error(`HTTP ${res.status}`);
    return await res.json();
  } catch (err) {
    // raw.githubusercontent throttles bursts; back off rather than fail the run
    if (attempt >= MAX) throw new Error(`${locale}/${bundle}: ${err.message}`);
    await sleep(250 * 2 ** (attempt - 1));
    return fetchBundle(locale, bundle, attempt + 1);
  }
}

/** Resolve tasks with a small concurrency cap so we don't trip rate limiting. */
async function pooled(tasks, limit = 6) {
  const queue = [...tasks];
  const workers = Array.from({ length: Math.min(limit, queue.length) }, async () => {
    while (queue.length) await queue.shift()();
  });
  await Promise.all(workers);
}

async function build() {
  const bundles = [...new Set(TERMS.map((t) => t.bundle))];
  const data = {};

  await pooled(
    Object.entries(LOCALES).flatMap(([docsTag, appTag]) =>
      bundles.map((b) => async () => {
        data[`${docsTag}|${b}`] = await fetchBundle(appTag, b);
      })
    )
  );

  const docsTags = Object.keys(LOCALES);
  const rows = [];
  const missing = [];

  for (const term of TERMS) {
    const label = term.note ? `${term.en} (${term.note})` : term.en;
    const cells = docsTags.map((tag) => {
      const v = data[`${tag}|${term.bundle}`]?.[term.key];
      if (!v) missing.push(`${tag} ${term.bundle} ${term.key}`);
      // a pipe in a value would break the table; none today, but don't emit broken markdown
      return v ? String(v).replace(/\|/g, '\\|') : '—';
    });
    rows.push(`| ${label} | ${cells.join(' | ')} |`);
  }

  if (missing.length) {
    console.warn(`⚠️  ${missing.length} missing string(s):\n   ${missing.join('\n   ')}`);
  }

  const table = [
    `| English | ${docsTags.join(' | ')} |`,
    `| --- | ${docsTags.map(() => '---').join(' | ')} |`,
    ...rows,
  ].join('\n');

  return [
    START,
    '',
    `Source of truth: [\`${REPO}\`](https://github.com/${REPO}) — the app's own translation`,
    'files. Regenerate with `node scripts/build-ui-glossary.js` when the app UI changes.',
    'These are the strings the app actually ships; do not "improve" them here.',
    '',
    table,
    '',
    END,
  ].join('\n');
}

(async () => {
  const block = await build();
  const src = fs.readFileSync(CONTEXT_FILE, 'utf8');
  const re = new RegExp(`${escapeRe(START)}[\\s\\S]*?${escapeRe(END)}`);

  if (!re.test(src)) {
    console.error(`❌ Could not find the glossary markers in ${CONTEXT_FILE}`);
    process.exit(1);
  }

  const next = src.replace(re, block);

  if (process.argv.includes('--check')) {
    if (next !== src) {
      console.error('❌ UI glossary is stale — run: node scripts/build-ui-glossary.js');
      process.exit(1);
    }
    console.log('✅ UI glossary is up to date');
    return;
  }

  fs.writeFileSync(CONTEXT_FILE, next);
  console.log(
    next === src
      ? '✅ UI glossary already up to date'
      : `✅ Wrote UI glossary (${TERMS.length} terms × ${Object.keys(LOCALES).length} locales)`
  );
})().catch((err) => {
  console.error(`❌ ${err.message}`);
  process.exit(1);
});

function escapeRe(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}
